package com.atlandes.admin.vo;

import java.util.Date;
import java.util.List;

/**
 * Created by XD.Wang on 2017/6/8.
 * 菜单
 */
public class MenuVO {

    private int id;

    private String code;

    private String name;

    private String remark;

    private String url;

    private int level;//级别

    private String parentCode;//父编号

    private int sortId;//排序

    private int isValid;//是否有效

    private int isVisible;//是否可见

    private Date createTime;

    private List<MenuVO> children;

    // 页面字段
    private String levelStr;
    private String isVisibleStr;

    public String getIsVisibleStr() {
        return isVisibleStr;
    }

    public void setIsVisibleStr(String isVisibleStr) {
        this.isVisibleStr = isVisibleStr;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getIsVisible() {
        return isVisible;
    }

    public void setIsVisible(int isVisible) {
        this.isVisible = isVisible;
    }

    public List<MenuVO> getChildren() {
        return children;
    }

    public void setChildren(List<MenuVO> children) {
        this.children = children;
    }

    public String getLevelStr() {
        return levelStr;
    }

    public void setLevelStr(String levelStr) {
        this.levelStr = levelStr;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getParentCode() {
        return parentCode;
    }

    public void setParentCode(String parentCode) {
        this.parentCode = parentCode;
    }

    public int getSortId() {
        return sortId;
    }

    public void setSortId(int sortId) {
        this.sortId = sortId;
    }

    public int getIsValid() {
        return isValid;
    }

    public void setIsValid(int isValid) {
        this.isValid = isValid;
    }
}
